letter_t
get_letter (uint8_t idx)
{
  if (idx >= NUM_LETTERS)
  {
    return letters[NUM_LETTERS];
  }
  return letters[idx];
}

hub75_color_t
get_letter_color (uint8_t idx)
{
  if (idx >= NUM_LETTERS)
  {
    return BLACK;
  }
  return letter_colors[idx];
}

Font_t
font_init (uint8_t index, int row)
{
  // get a random number
//  int rand_idx = gen_rand_int (0, 6);

  // initialize the piece values
  Font_t new_font;
  if(row)
  {
	  new_font.x_coord = 24;
  }
  else
  {
	  new_font.x_coord = 1;
  }
  new_font.y_coord = 2;
  new_font.letter = get_letter (index);
  new_font.color = get_letter_color (index);
  return new_font;
}


